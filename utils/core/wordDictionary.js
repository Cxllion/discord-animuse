/**
 * Checks if a word exists in the offline dictionary.
 * @param {string} word 
 * @returns {object|null}
 */
function getOfflineWordData(word) {
    if (!word) return null;
    return OFFLINE_DICTIONARY.find(entry => entry.word === word.toUpperCase()) || null;
}

/**
 * Picks a random 5-letter word from the archive.
 * @returns {object}
 */
